def compress_to_rnd_suffix(list_in):
  if list_in:
    grouped_files = defaultdict(list); pattern = compile(r"^(.*)(_rnd\d+\.wav)$"); final_list = []
    for file in list_in:
      match = pattern.match(file)
      if match:base_name = match.group(1); grouped_files[base_name].append(file)
