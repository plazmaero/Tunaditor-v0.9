def chop_surface(surface, sub_width, sub_height):
  surface_width, surface_height = surface.get_size()
  chopped_surfaces = []

  for y in range(0, surface_height, sub_height):
    for x in range(0, surface_width, sub_width):
      try:
        sub_surface = surface.subsurface((x, y, sub_width, sub_height)).copy()
        chopped_surfaces.append(sub_surface)
      except: pass

  return chopped_surfaces

