def arabic_numeral(input_string):
  nums = []
  for i_char, char in enumerate(input_string):
    if char.isnumeric():
      if i_char != 0 and input_string[i_char-1].isnumeric(): nums[-1][0] += char
      else: nums.append([char, i_char])
    elif i_char != 0 and input_string[i_char-1].isnumeric(): nums[-1].append(i_char)
            
  for num in nums:
    if len(num) == 3: input_string = input_string[:num[1]]+num[0][::-1]+input_string[num[2]:]
    else: input_string = input_string[:num[1]]+num[0][::-1]
  
  return input_string

