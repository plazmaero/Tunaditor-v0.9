def replace_letter(string, letter_to_replace, replacement_letter):
  new_string = ""
  for char in string:
    if char == letter_to_replace: new_string += replacement_letter
    else: new_string += char
  return new_string

def get_key_from_value(dictionary, value):
  for key, val in dictionary.items():
    if val == value: return key
  return None

def contains_all(a, b):
  ca, cb = Counter(a), Counter(b)
  return all(ca[x] >= cb[x] for x in cb)

def get_material_name(input_string):
  # Common substrings to split on
  substrings = ["terrain", "row", "column", "block"]
  # Find the index of the first occurrence of any of the substrings
  index = min(input_string.find(sub) for sub in substrings if sub in input_string)
  # Extract the material name before the first occurrence of any of the substrings
  material_name = input_string[:index].strip()
  return material_name

